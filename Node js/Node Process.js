// Node,js Process

// node.js provides the facility to get process information such as process id, architecture,
// platform,version,release,uptime,upu usage etc.It can also be used to kill process ,set uid
// set group, unmask etc

// The process is a global object an instance of EventEmitter can be accessed from any where

// Node.js Process Properties
// A list of commonly used Node.js process properties are given below

// 1 arch = return process architecture
// 2 args = return commands line arguments as an array
// 3 env = return user environment 
// 4 pid = return process id of the process
// 5 platform = return platform of the process : 'darwin' 'freebsd', 'linux' etc
// 6 release = return the metadata for the current node release
// 7 version = return the node version
// 8 versions = return the node version and its dependencies
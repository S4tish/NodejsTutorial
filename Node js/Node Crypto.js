// What is Hash
// A hash is a fixed -length string of bits i.e procedurally and deterministically generated from
// some arbitrary block of source data

// What is HMAC
// HMAC stands for hash-based message Authentication code. It is a process for applying a hash 
// algorithm to both data and a secret key that result in a single final hash